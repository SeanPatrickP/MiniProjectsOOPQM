class Vehicle {
    private double horsepower;
    private double aerodynamics;
    private double wieght;

public Vehicle(double arg1, double arg2, double arg3) {
    horsepower = arg1;
    aerodynamics = arg2;
    wieght = arg3;
}
public double gethorsepower(){   
    return(horsepower);
}
public double getaerodynamics(){   
    return(aerodynamics);
}    
public double getwieght(){   
    return(wieght);
}        
}